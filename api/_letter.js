export function describeAnketa(answers, archetypeId) {
  const c = answers.contact || {};
  const archName = ARCHETYPES[archetypeId] || 'неопределён';
  const pains = (answers.pains || []).map((p) => PAIN[p] || p);

  const dream = (answers.dream || '').trim();
  const obstacles = (answers.obstacles || '').trim();
  const tried = (answers.tried || '').trim();

  const lines = [
    'ФАКТЫ ИЗ АНКЕТЫ',
    `Имя: ${c.name || '—'}`,
    `Профессия: ${PROF[answers.prof] || '—'}`,
    `Этап дела: ${ETAP[answers.etap] || '—'}`,
    `Доход: ${INCOME[answers.income] || '—'}`,
    `Отношения: ${RELATIONS[answers.relations] || '—'}`,
    `Дети: ${FAMILY[answers.family] || '—'}`,
    `Основные боли (что тяжелее всего): ${pains.length ? pains.join('; ') : '—'}`,
    `Назначенный архетип: ${archName}`,
    '',
    'ЕЁ СОБСТВЕННЫЕ СЛОВА (используйте как опорный слой)',
    '',
    'Мечта / что для неё важно в женской реализации:',
    dream ? `«${dream}»` : '—',
    '',
    'Препятствия — что стоит между ней и этой мечтой сейчас:',
    obstacles ? `«${obstacles}»` : '—',
    '',
    'Что уже пробовала и что из этого помогало / не помогало:',
    tried ? `«${tried}»` : '—'
  ];
  return lines.join('\n');
}

