function describeAnketa(answers, archetypeId) {
  const c = answers.contact || {};
  const archName = ARCHETYPES[archetypeId] || 'неопределён';
  const pains = (answers.pains || []).map((p) => PAIN[p] || p);
  const exp = (Array.isArray(answers.experience) ? answers.experience : [answers.experience])
    .filter(Boolean)
    .map((e) => EXPERIENCE[e] || e);

  const lines = [
    `Имя: ${c.name || '—'}`,
    `Профессия: ${PROF[answers.prof] || '—'}`,
    `Этап дела: ${ETAP[answers.etap] || '—'}`,
    `Доход: ${INCOME[answers.income] || '—'}`,
    `Отношения: ${RELATIONS[answers.relations] || '—'}`,
    `Дети: ${FAMILY[answers.family] || '—'}`,
    `Основные боли (что тяжелее всего): ${pains.length ? pains.join('; ') : '—'}`,
    `Опыт работы с собой: ${exp.length ? exp.join('; ') : '—'}`,
    `Готовность к ежедневной практике 30–60 мин: ${READINESS[answers.readiness] || '—'}`,
    '',
    `Назначенный архетип: ${archName}`
  ];
  return lines.join('\n');
}

export async function generateLetter(answers, archetypeId, { timeoutMs = 25000 } = {}) {
  if (!process.env.ANTHROPIC_API_KEY) {
    return { text: null, debug: 'no_api_key' };
  }

  const userText = describeAnketa(answers, archetypeId) + '\n\nНапишите ей письмо-результат по правилам.';

  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  const t0 = Date.now();

  try {
    const response = await anthropic.messages.create(
      {
        model: 'claude-sonnet-4-6',
        max_tokens: 2000,
        thinking: { type: 'disabled' },
        system: SYSTEM_PROMPT,
        messages: [{ role: 'user', content: userText }]
      },
      { signal: controller.signal }
    );
    clearTimeout(timer);

    const textBlock = response.content.find((b) => b.type === 'text');
    const text = textBlock?.text?.trim();
    if (!text) return { text: null, debug: 'empty_text', ms: Date.now() - t0 };

    return {
      text: text.replace(/—/g, '–'),
      debug: 'ok',
      ms: Date.now() - t0
    };
  } catch (err) {
    clearTimeout(timer);
    const msg = err?.message || String(err);
    const status = err?.status || err?.response?.status;
    console.error('generateLetter failed:', msg);
    return { text: null, debug: `error:${status || 'noStatus'}:${msg.slice(0, 200)}`, ms: Date.now() - t0 };
  }
}
