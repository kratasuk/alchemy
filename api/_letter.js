export function describeAnketa(answers, archetypeId) {
  const c = answers.contact || {};
  const name = c.name || '';
  const painsEng = (answers.pains || []).map((p) => PAINS[p] || p);
  const contextualParam = deriveContextualParam(answers.pains);

  const dream = (answers.dream || '').trim();
  const obstacles = (answers.obstacles || '').trim();
  const tried = (answers.tried || '').trim();

  const lines = [
    `NAME: ${name}`,
    `PROFESSION: ${PROFESSION[answers.prof] || ''}`,
    `STAGE: ${STAGE[answers.etap] || ''}`,
    `INCOME: ${INCOME[answers.income] || ''}`,
    `RELATIONSHIPS: ${RELATIONSHIPS[answers.relations] || ''}`,
    `CHILDREN: ${CHILDREN[answers.family] || ''}`,
    `PAINS: [${painsEng.join(', ')}]`,
    '',
    `DREAM: ${dream}`,
    `OBSTACLES: ${obstacles}`,
    `TRIED: ${tried}`,
    '',
    `ARCHETYPE: ${ARCHETYPES[archetypeId] || ''}`,
    `READINESS: green`,
    `CONTEXTUAL_PARAM: ${contextualParam}`,
    `MATCHED_GRADUATE: `,
    '',
    'Напишите письмо.'
  ];
  return lines.join('\n');
}
